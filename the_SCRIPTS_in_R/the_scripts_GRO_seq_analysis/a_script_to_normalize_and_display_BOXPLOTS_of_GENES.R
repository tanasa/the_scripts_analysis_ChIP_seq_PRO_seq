all <- read.delim(ALL, header=T, sep = "\t", stringsAsFactors = F)
colnames(all)

rownames(all) <- all$Symbol.x
all_PR <- all[, c(24,25)]

colnames(all_PR )
#############################################################################################################
############################################################################################################# normalization in edgeR :

colnames(all_PR) <- c("minus", "plus")
group <- factor(c("minus", "plus"))
libSizes <- as.vector(colSums(all_PR))

y <- DGEList(counts=all_PR, group=group, lib.size=libSizes)
z <- y

############################################################################################################
############################################################################################################

# y <- calcNormFactors(y)
# counts.per.m <- cpm(y, normalized.lib.sizes=TRUE)

############################################################################################################
############################################################################################################ FILTERING :

keep <- rowSums(cpm(z) > 3) >= 1
z <- z[keep, ,keep.lib.sizes=FALSE]
dim(z)

############################################################################################################
############################################################################################################ NORMALIZATION :

z <- calcNormFactors(z)
counts.per.m.z <- cpm(z, normalized.lib.sizes=TRUE)
counts.per.m.z <- as.data.frame(counts.per.m.z)

###################### display the NORMALIZED DATA :

png(paste(TITLE_HERE, "after.normalization.after.filtering.png", sep="."))
boxplot(counts.per.m.z[,1], 
        counts.per.m.z[,2],
        ylim=c(0,200),
        col="yellow", 
        main=" NORMALIZED COUNTS in edgeR after filtering")
dev.off()

############################################################################################################
############################################################################################################

wilcox.test(counts.per.m.z$minus, counts.per.m.z$plus)

